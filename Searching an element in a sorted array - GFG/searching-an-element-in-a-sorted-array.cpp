class Solution{
    public:
    // Function to find element in sorted array
    // arr: input array
    // N: size of array
    // K: element to be searche
    int searchInSorted(int arr[], int N, int K) 
    { 
        if(N==1) return K==arr[0]?1:-1;
       // Your code here
       int l=-1,r=N;
       while(l+1<r){
           int mid = (l+r)/2;
           if(arr[mid]<K) l=mid;
           else if(arr[mid]==K) return 1;
           else r=mid;
       }
       return -1;
    }
};

